// var : global scope. can be accessed anywhere in the code. can be redeclared and updated.
// let : block scope. can be accessed only within the block it is declared. can be updated but not redeclared.
// const : block scope. can be accessed only within the block it is declared. cannot be updated or redeclared.


let gameName = "Super Mario";
gameName = "Super Mario 3D World";
console.log(gameName);

const userName = "Gaurav Waghmare";
console.log(userName);